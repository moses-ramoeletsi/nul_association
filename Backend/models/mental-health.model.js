import mongoose, { Schema } from "mongoose";

const healthcareSchema = new Schema(
  {
    title: {
      type: String,
      required: true,
      trim: true,
    },
    description: {
      type: String,
      required: true,
    },
    date: {
      type: String,
      required: true,
    },
    location: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

const HealthCare = mongoose.model("HealthCare", healthcareSchema);
export default HealthCare;
